const getComponentCardColours = (name, variable, hex) => /*html*/`
    <bds-card-color name=${name} variable=${variable} hex=${hex}></bds-card-color>
`;

const getColorsCards = (colors) => {
    let html = '';

    for (let color of colors) {
        html += getComponentCardColours(color.name, color.variable, color.hex);
    }
    return html;
}

